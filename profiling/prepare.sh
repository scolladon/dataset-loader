#!/usr/bin/env bash
# Prepare the profiling environment before each run:
#   1. generate a fresh .dataset-load.state.json whose watermarks are set to
#      (midnight UTC, N days ago) — default 7 days. This keeps every
#      profiling run pulling the same window size regardless of calendar date.
#   2. remove CSV output files produced by file-target entries.
# Run from the project root: bash profiling/prepare.sh [days]
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
ROOT_DIR="$(dirname "$SCRIPT_DIR")"
DAYS="${1:-7}"

STATE_FILE="$ROOT_DIR/.dataset-load.state.json"

# Compute the watermark once, in Node (cross-platform date arithmetic).
WATERMARK=$(node -e "
  const d = new Date();
  d.setUTCDate(d.getUTCDate() - Number(process.argv[1]));
  d.setUTCHours(0, 0, 0, 0);
  console.log(d.toISOString().replace('Z', '+0000'));
" "$DAYS")

cat > "$STATE_FILE" <<EOF
{
  "watermarks": {
    "pageviews-xrmru": "$WATERMARK",
    "pageviews-prod": "$WATERMARK"
  }
}
EOF
echo "State prepared: pageviews-{xrmru,prod} watermark = $WATERMARK (${DAYS} days ago, 00:00 UTC)"

# Remove CSV output files generated by file-target entries so subsequent runs
# write from scratch rather than appending to stale content.
rm -f \
  "$ROOT_DIR/AllLightningPageView.csv" \
  "$ROOT_DIR/ProdLightningPageView.csv" \
  "$ROOT_DIR/XrmruUser.csv"
echo "CSV output files removed"

echo "Ready for next run."
